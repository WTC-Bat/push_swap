int		ft_atoi(char *s)
{
	int		cnt;
	int		out_int;

	cnt = 0;
	out_int = 0;
	while (s[cnt])
	{
		out_int = out_int * 10 + (s[cnt] - '0');
		cnt++;
	}
	return (out_int);
}
